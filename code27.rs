fn main() {
    let a: u8 = 0b1010_1010;
    let b: u8 = 0b1100_1100;

    println!("a & b = {:08b}", a & b); // 1000_1000
    println!("a | b = {:08b}", a | b); // 1110_1110
    println!("a ^ b = {:08b}", a ^ b); // 0110_0110
    println!("!a    = {:08b}", !a);    // 0101_0101
    println!("a << 1 = {:08b}", a << 1); // 0101_0100
    println!("a >> 1 = {:08b}", a >> 1); // 0101_0101
}
